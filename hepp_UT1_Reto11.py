# Calculadora sencilla
# Pide al usuario dos números. Muestra la suma, la resta, la multiplicación y la división.

numero1 = int(input("Por favor ingrese el primer número: "))
numero2 = int(input("Por favor ingrese el segundo número: "))

print("La suma de ambos números es:", numero1 + numero2)
print("La resta de ambos números es:", numero1 - numero2)
print("La multiplicación de ambos números es:", numero1 * numero2)
print("La división de ambos números es:", numero1 / numero2)