# Crea un algoritmo que pida dos números e indique si el primero es mayor que el segundo

numero1 = int(input("Por favor, introduzca el primer número: "))

numero2 = int(input("Por favor, introduzca el segundo número: "))

if numero1 > numero2:
    print("El primero número es mayor que el segundo número.")
else:
    print("El primero número no es mayor que el segundo número.")